import { PROMPT_DEX_SCHEMA_VERSION, type PromptEntry } from '@/lib/tools/prompt-dex/types'

/**
 * 初期収録プロンプト(builtin)。
 *
 * SunoとUdioで「効き方」が違うため、主要ジャンルはサービス別に作り分けている。
 * - Suno: スタイル欄は4〜7語のカンマ区切り(ジャンル→テンポ→楽器→ボーカル→質感→雰囲気)。
 *   BPMは目安程度。不要な音は Style ではなく Exclude Styles 欄へ。構成は歌詞欄の [Verse]/[Chorus] タグ。
 * - Udio: 情景を文章で描写する自然言語型(セッションミュージシャンに指示する感覚)。
 *   Prompt Strength で忠実度を調整、強調したい語は繰り返す、精密制御は Manual Mode。
 *
 * 実在する存命アーティストの模倣は入れない(色・雰囲気・構成で表現)。
 * createdAt は収録順を安定させるための固定値。
 */

const D = '2026-07-22T00:00:00.000Z'

function b(
  id: string,
  e: Omit<PromptEntry, 'id' | 'createdAt' | 'updatedAt' | 'source' | 'schemaVersion'>,
): PromptEntry {
  return {
    id,
    ...e,
    createdAt: D,
    updatedAt: D,
    source: 'builtin',
    schemaVersion: PROMPT_DEX_SCHEMA_VERSION,
  }
}

